/******************************************************************************
* \Syntax          : void NVIC_Init(NVIC_IRQs IRQ)
* \Description     : This function initializes the interrupt according to the user's intput.
*
* \Sync\Async      : Synchronous
* \Reentrancy      : Non Reentrant
* \Parameters (in) : NVIC_IRQs IRQ
* \Parameters (out): Void
* \Return value:   : Void
*
*******************************************************************************/
void NVIC_Init(NVIC_IRQs IRQ)
{
	static u8 IRQ_NUM;
	u16 IRQ_Handler=(u16)IRQ;
	
	IRQ_NUM =	(IRQ%32);
	
	if(IRQ_Handler >= 0 && IRQ_Handler < 32)
	{
		NVIC_SET_EN0_REG |= (1 << IRQ_NUM);
	}
	else if(IRQ_Handler >= 32 && IRQ_Handler < 64)
	{
		NVIC_SET_EN1_REG |= (1 << IRQ_NUM);
	}
	else if(IRQ_Handler >= 64 && IRQ_Handler < 96)
	{
		NVIC_SET_EN2_REG |= (1 << IRQ_NUM);
	}
	else if(IRQ_Handler >= 96 && IRQ_Handler < 128)
	{
		NVIC_SET_EN3_REG |= (1 << IRQ_NUM);
	}
	else if(IRQ_Handler >= 128 && IRQ_Handler < 138)
	{
		NVIC_SET_EN4_REG |= (1 << IRQ_NUM);
	}
}

/******************************************************************************
* \Syntax          : void Global_Interrupt_Enable(void)
* \Description     : This function enables the global interrupts
*
* \Sync\Async      : Synchronous
* \Reentrancy      : Non Reentrant
* \Parameters (in) : Void
* \Parameters (out): Void
* \Return value:   : Void
*
*******************************************************************************/
void Global_Interrupt_Enable(void)
{
	__asm("CPSIE I");
}





